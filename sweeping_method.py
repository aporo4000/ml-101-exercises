# 掃き出し法
import numpy as np

x = np.array([[2,2,3,10],[8,6,4,12],[11,8,6,14]])
x[0] = x[0]/x[0][0]
x[1] = x[1] - x[0]*x[1][0]
x[2] = x[2] - x[0]*x[2][0]
x[1] = x[1]/x[1][1]
x[2] = x[2] - x[1]*x[2][1]
x[2] = x[2]/x[2][2]

print(x)
#>>> x
#array([[ 1,  1,  1,  5],
#       [ 0,  1,  2, 14],
#       [ 0,  0,  1,  1]])

x_3 = x[2][2]
x_2 = x[1][3] - x[1][2]*x_3
